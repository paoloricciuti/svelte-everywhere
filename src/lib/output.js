/**
 * @param {Array<TemplateStructure>} structure
 * @param {number} flags
 * @param {'svg' | 'math'} ns
 * @returns {() => Node | Node[]}
 */
/*#__NO_SIDE_EFFECTS__*/
function ns_template_fn(structure, flags, ns = 'svg') {
	var is_fragment = (flags & TEMPLATE_FRAGMENT) !== 0;

	/** @type {Element | DocumentFragment} */
	var node;

	return () => {
		if (!node) {
			var fragment = fragment_from_tree(structure);

			if (is_fragment) {
				node = document.createDocumentFragment();
				while (get_first_child(fragment)) {
					node.appendChild(/** @type {Node} */ (get_first_child(fragment)));
				}
			} else {
				node = /** @type {Element} */ (get_first_child(fragment));
			}
		}

		var clone = /** @type {TemplateNode} */ (node.cloneNode(true));

		if (is_fragment) {
			var start = /** @type {TemplateNode} */ (get_first_child(clone));
			var end = /** @type {TemplateNode} */ (clone.lastChild);

			assign_nodes(start, end);
		} else {
			assign_nodes(clone, clone);
		}

		return clone;
	};
}

