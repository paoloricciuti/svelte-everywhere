const ESC = '\u001b[';
const CLEAR = ESC + '2J' + ESC + '0;0H'; // Clear screen and move cursor to top-left

const ansi_colors = {
	red: ESC + '31m',
	green: ESC + '32m',
	yellow: ESC + '33m',
	blue: ESC + '34m',
	magenta: ESC + '35m',
	cyan: ESC + '36m',
	white: ESC + '37m',
	reset: ESC + '0m'
};

const colors = Object.keys(ansi_colors).filter((color) => color !== 'reset');

let top_fragment;

let current_string = '';

function render_element(element) {
	if (element.attributes?.color) {
		current_string += ansi_colors[element.attributes.color];
	}
	element.render?.();
	if (element.attributes?.color) {
		current_string += ansi_colors.reset;
	}
}

let scheduled = false;

function schedule_render() {
	if (!scheduled) {
		scheduled = true;
		queueMicrotask(() => {
			top_fragment.render();
			scheduled = false;
		});
	}
}

