/**
 *
 * @param {*} structure
 * @returns
 */
function fragment_from_tree(structure) {
	var fragment = create_fragment();

	for (var item of structure) {
		if (typeof item === 'string') {
			append(null, create_text(item), fragment);
			continue;
		}

		// if `preserveComments === true`, comments are represented as `['// <data>']`
		if (item === undefined || item[0][0] === '/') {
			append(null, create_comment(item ? item[0].slice(3) : ''), fragment);
			continue;
		}

		const [name, attributes, ...children] = item;

		var element = create_element(name, attributes?.is);

		for (var key in attributes) {
			set_attribute(element, key, attributes[key]);
		}

		if (children.length > 0) {
			append(null, fragment_from_tree(children), element);
		}

		append(null, element, fragment);
	}

	return fragment;
}

/**
 * @param {Array<TemplateStructure>} structure
 * @param {number} flags
 * @returns {() => Node | Node[]}
 */
/*#__NO_SIDE_EFFECTS__*/
function from_tree(structure, flags) {
	var is_fragment = (flags & TEMPLATE_FRAGMENT) !== 0;

	/** @type {Node} */
	var node;

	return () => {
		node = fragment_from_tree(structure);
		if (!is_fragment) node = /** @type {Node} */ (get_first_child(node));

		if (is_fragment) {
			var start = /** @type {TemplateNode} */ (get_first_child(node));
			var end = /** @type {TemplateNode} */ (node.lastChild);

			assign_nodes(start, end);
		} else {
			assign_nodes(node, node);
		}

		return node;
	};
}

